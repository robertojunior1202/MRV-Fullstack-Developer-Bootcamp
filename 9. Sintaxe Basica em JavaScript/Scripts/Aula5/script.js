//TIPOS DE FUNCOES
//Funcoes declarativas; tem que ter um nome
function nomeFuncao (){
    console.log("Mostra Mensagem!")
}
nomeFuncao()

//Expressoes de funcoes; ter nome e opcional, pois o JS atribui o nome da variavel a funcao
var funcao = function(){
    console.log("Sou uma mensagem da funcao expressao")
}
funcao();

//Arrow Function;
var funcao = () =>{
console.log("Sou uma arrow function!!!")
}

